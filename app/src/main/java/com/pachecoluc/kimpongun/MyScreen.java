package com.pachecoluc.kimpongun;

public class MyScreen {
    float width;
    float height;

    public MyScreen(float w, float h){
        this.width = w;
        this.height = h;
    }

    public float getWidth() {
        return this.width;
    }

    public void setWidth(float w) {
        this.width = w;
    }

    public float getHeight() {
        return this.height;
    }

    public void setHeight(float h) {
        this.height = h;
    }
}
